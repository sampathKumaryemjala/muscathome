                <p>One of the advantages of renting is that if your earning power suddenly changes, it is relatively easy to adjust your living situation accordingly. Lose your job and have to take a pay cut to find a new one? That isn’t much fun, but it’s less painful if it results in moving into a smaller apartment rather than having your house foreclosed upon and your credit wrecked.</p>
