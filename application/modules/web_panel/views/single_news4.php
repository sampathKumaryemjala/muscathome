                 <strong>Is the home you are buying permanent or only for a short period of time?</strong><br>
                 <p>One of the nonfinancial benefits of buying a home is that you know you can live in it indefinitely. You don’t have to worry that the landlord will raise your rent 20 percent, or demolish the building to turn it into something else, or just kick you out for his son to move in. You can renovate the kitchen or paint the shutters according to your preference and yours alone.</p>
                 <p>So what is that worth to you? This is a question that defies any attempt to analyze using a spreadsheet. It is purely a question of your preferences and priorities in life.</p>
                 <strong>How confident are you that you will want to stay?</strong><br>
                 <p>One of the biggest factors shaping the desirability of buying versus renting is how long you will stay in a place. Given the huge costs, both financial and psychic, of selling a house, it is generally a far better deal to buy if you will live in a place for a decade than if you will be moving in three years.</p>
                 <p>But simply putting your best guess of how long you will stay gives you a limited view of things. We all actually have a range of possibilities. What are the odds that a year from now you will get offered a dream job in Sohar or Salalah, or meet the love of your life who already has a great place, or conversely go through a break-up or divorce?</p>
                 <p>Our best plans are still only guesses, but some people are more settled and less likely to have a sudden move than others (a person who is absolutely committed to staying in his or her current city, for example, or a happily married couple with all the children they want). Be honest with yourself about how settled, or unsettled, your future living situation really is.</p>
